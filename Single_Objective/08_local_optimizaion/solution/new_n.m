function nmn(x)

g = ones(2, 1);
h = 1e-3;

for i = 1:10
    g(1,1) = (rb([x(1,1)+h;x(2,1)])  - rb([x(1,1)-h;x(2,1)]))   / (2*h);
    g(2,1) = (rb([x(1,1);  x(2,1)+h])- rb([x(1,1);  x(2,1)-h])) / (2*h);
    
    H(1,1) = (rb([x(1,1)+2*h;x(2,1)])    - 2*rb([x(1,1);x(2,1)])  + rb([x(1,1)-2*h;x(2,1)])) / (4*h^2);
    H(1,2) = (rb([x(1,1)+h;  x(2,1)+h])  - rb([x(1,1)-h;x(2,1)+h])- rb([x(1,1)+h;  x(2,1)-h]) + rb([x-h;x(2,1)-h])) / (4*h^2);
    H(2,1) =  H(1,2);
    H(2,2) = (rb([x(1,1);    x(2,1)+2*h])- 2*rb([x(1,1);x(2,1)])  + rb([x(1,1);   x(2,1)-2*h])) / (4*h^2);
    
    x= x - inv(H)*g;
    out(i,:) = x';
end

M = size(out, 1); figure
plot(1:M, out(:,1), 'r-', 1:M, out(:,2), 'b-');
end